package com.example.webdavplayer.ui.theme

import androidx.compose.ui.unit.dp

/**
 * 间距规范：以 4dp 为最小栅格基准，统一全 App 的留白与节奏。
 *
 * 使用建议：
 * - 列表类页面横向内边距用 [md]，列表项间距用 [xs]；
 * - 表单 / 设置类页面整体内边距用 [lg]，分组间距用 [md]。
 */
object Spacing {
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 24.dp
    val xxl = 32.dp
}
